# How do we create an iterator? What exactly is an iterator?
# 1. Sequence - list, dictionary, float, and so on.
# 2. Use the range function. Allows us to create an iterator from numbers (integers)
    # range(start, stop, step)


# Write a program that prints all even numbers between 1 and 100
for i in range(2, 100, 2):  # [2, 4, 6, 8, ...]
    print(i)





